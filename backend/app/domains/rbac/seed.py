@dataclass
class SeedSummary:
    permission_groups_created: int = 0
    permissions_created: int = 0
    permission_scopes_created: int = 0
    roles_created: int = 0
    role_permissions_created: int = 0


async def seed_rbac(session: AsyncSession) -> SeedSummary:
    """Idempotently seed permission groups, permissions, permission scopes,
    and the system roles (with their permissions). Safe to call repeatedly:
    every insert is preceded by an existence check."""
    repository = RBACRepository(session)
    summary = SeedSummary()

    group_by_module: dict[PermissionModule, PermissionGroup] = {}
    for module in PermissionModule:
        group = await repository.get_permission_group_by_key(module.value)
        if group is None:
            group = await repository.create_permission_group(
                key=module.value,
                name=MODULE_DISPLAY_NAMES[module],
                sort_order=list(PermissionModule).index(module),
            )
            summary.permission_groups_created += 1
        group_by_module[module] = group

    permission_by_key: dict[str, Permission] = {}
    for module, actions in MODULE_ACTIONS.items():
        group = group_by_module[module]
        for action in actions:
            key = permission_key(module, action)
            permission = await repository.get_permission_by_key(key)
            if permission is None:
                permission = await repository.create_permission(
                    permission_group_id=group.id,
                    key=key,
                    action=action.value,
                    name=f"{MODULE_DISPLAY_NAMES[module]}: {action.value.title()}",
                    description=(
                        f"{action.value.title()} access for the "
                        f"{MODULE_DISPLAY_NAMES[module]} module."
                    ),
                )
                summary.permissions_created += 1

            existing_scopes = set(
                await repository.get_permission_scope_types(permission.id)
            )
            for scope_type in allowed_scope_types_for_module(module):
                if scope_type.value not in existing_scopes:
                    await repository.add_permission_scope(permission.id, scope_type)
                    summary.permission_scopes_created += 1

            permission_by_key[key] = permission

    for role_def in SYSTEM_ROLES:
        role = await repository.get_role_by_slug(role_def.slug, None)
        if role is None:
            role = await repository.create_role(
                name=role_def.name,
                slug=role_def.slug,
                description=role_def.description,
                is_system_role=True,
                is_template=False,
                is_active=True,
                scope_type=role_def.scope_type.value,
                organization_id=None,
                parent_role_id=None,
            )
            summary.roles_created += 1

        existing_permission_ids = {
            row.permission_id for row in await repository.get_role_permissions(role.id)
        }
        for module, actions in role_def.grants().items():
            for action in actions:
                permission = permission_by_key[permission_key(module, action)]
                if permission.id not in existing_permission_ids:
                    await repository.add_role_permission(
                        role.id, permission.id, granted_by=None
                    )
                    summary.role_permissions_created += 1

    logger.info("rbac_seed_completed", extra=vars(summary))
    return summary


def generate_permission_matrix_markdown() -> str:
    """Render ``docs/rbac/PERMISSION_MATRIX.md`` content directly from
    :data:`SYSTEM_ROLES` / :data:`MODULE_ACTIONS`, so the doc can never drift
    from the actual seed data -- regenerate it by re-running this function
    (see ``docs/rbac/PERMISSION_MATRIX.md``'s header for the exact command)."""
    lines: list[str] = [
        "# RBAC Permission Matrix",
        "",
        "Generated directly from `app/domains/rbac/seed.py` "
        "(`SYSTEM_ROLES` x `MODULE_ACTIONS`) -- do not hand-edit the table "
        "below. Regenerate with:",
        "",
        "```bash",
        "cd backend",
        'python -c "from app.domains.rbac.seed import '
        'generate_permission_matrix_markdown as g; print(g())" '
        "> docs/rbac/PERMISSION_MATRIX.md",
        "```",
        "",
        "Cell values are the *default grant level* for that role x module "
        "pair: `-` (none), `R` (read-only), `O` (operate: applicable actions "
        "minus Manage/Delete), or `F` (full: every applicable action for "
        "that module). See `RBAC_ARCHITECTURE.md` for what each level "
        "expands to.",
        "",
    ]

    header = ["Role", "Scope"] + [MODULE_DISPLAY_NAMES[m] for m in PermissionModule]
    lines.append("| " + " | ".join(header) + " |")
    lines.append("|" + "|".join(["---"] * len(header)) + "|")

    level_symbol = {
        GrantLevel.NONE: "-",
        GrantLevel.READ: "R",
        GrantLevel.OPERATE: "O",
        GrantLevel.FULL: "F",
    }
    for role_def in SYSTEM_ROLES:
        row = [role_def.name, role_def.scope_type.value]
        for module in PermissionModule:
            level = role_def.overrides.get(module, role_def.default_level)
            row.append(level_symbol[level])
        lines.append("| " + " | ".join(row) + " |")

    lines.append("")
    lines.append("## Module -> Applicable Actions")
    lines.append("")
    lines.append("| Module | Applicable actions |")
    lines.append("|---|---|")
    for module, actions in MODULE_ACTIONS.items():
        lines.append(
            f"| {MODULE_DISPLAY_NAMES[module]} | "
            f"{', '.join(action.value for action in actions)} |"
        )
    lines.append("")

    return "\n".join(lines)


async def _main() -> None:
    """``python -m app.domains.rbac.seed`` entrypoint."""
    from app.database.session import SessionLocal

    async with SessionLocal() as session:
        summary = await seed_rbac(session)
        await session.commit()
        logger.info("rbac_seed_summary", extra=vars(summary))
        print(summary)  # noqa: T201 -- CLI entrypoint output


if __name__ == "__main__":
    import asyncio

    asyncio.run(_main())
