"""Data-driven RBAC seed data and an idempotent seeding entrypoint.

Nothing in this module is a hand-written list of individual permission or
role-permission rows. Instead:

* :data:`MODULE_ACTIONS` maps each :class:`PermissionModule` to the subset of
  :class:`PermissionAction` values that apply to it (e.g. ``Approve`` makes
  sense for ``Voucher``/``Campaigns``/``Router Provisioning`` but not for
  ``Dashboard``). ``permissions`` rows are generated by iterating this
  mapping.
* :data:`MODULE_NARROWEST_SCOPE` maps each module to the narrowest
  :class:`ScopeType` at which it is meaningful (e.g. ``Guest Users`` is
  meaningful down to ``LOCATION``; ``System Settings`` only at ``GLOBAL``).
  ``permission_scopes`` rows are generated from this -- a permission is
  valid at its module's narrowest scope *and every broader scope* (a
  GLOBAL-scoped role can always exercise a location-level permission; the
  reverse is not true). This is what keeps e.g. "Super Admin holds every
  permission" from ever conflicting with a narrower permission_scopes
  constraint: GLOBAL is always in range.
* :data:`SYSTEM_ROLES` describes each of the 15 seeded system roles as a
  *default grant level per module, with per-module overrides*
  (:class:`GrantLevel`: ``NONE`` / ``READ`` / ``OPERATE`` / ``FULL``) rather
  than a literal permission-key list. ``role_permissions`` rows are
  generated by expanding each role's levels against ``MODULE_ACTIONS``.

Run via ``python -m app.domains.rbac.seed`` or by calling
:func:`seed_rbac` with an ``AsyncSession`` (e.g. from an application
startup hook or a one-off script). Safe to run repeatedly: every insert is
guarded by an existence check first.
"""

