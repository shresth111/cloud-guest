def permission_key(module: PermissionModule, action: PermissionAction) -> str:
    return f"{module.value}.{action.value}"


def allowed_scope_types_for_module(module: PermissionModule) -> tuple[ScopeType, ...]:
    """Every scope from ``GLOBAL`` down to the module's narrowest scope, inclusive."""
    narrowest_order = SCOPE_HIERARCHY_ORDER[MODULE_NARROWEST_SCOPE[module]]
    return tuple(
        scope for scope in ScopeType if SCOPE_HIERARCHY_ORDER[scope] <= narrowest_order
    )


# ============================================================================
# System roles: default grant level per module, with overrides
# ============================================================================


class GrantLevel(StrEnum):
    """How much of a module's applicable actions a role gets by default."""

    NONE = "none"
    READ = "read"
    OPERATE = "operate"
    FULL = "full"


def expand_grant_level(
    level: GrantLevel, actions: tuple[PermissionAction, ...]
) -> tuple[PermissionAction, ...]:
    if level == GrantLevel.NONE:
        return ()
    if level == GrantLevel.READ:
        return tuple(
            a for a in actions if a in (PermissionAction.READ, PermissionAction.VIEW)
        )
    if level == GrantLevel.OPERATE:
        return tuple(
            a
            for a in actions
            if a not in (PermissionAction.MANAGE, PermissionAction.DELETE)
        )
    return actions  # FULL


@dataclass(frozen=True)
class SystemRoleDefinition:
    name: str
    slug: str
    description: str
    scope_type: ScopeType
    default_level: GrantLevel
    overrides: Mapping[PermissionModule, GrantLevel] = field(default_factory=dict)

    def grants(self) -> dict[PermissionModule, tuple[PermissionAction, ...]]:
        resolved: dict[PermissionModule, tuple[PermissionAction, ...]] = {}
        for module, actions in MODULE_ACTIONS.items():
            level = self.overrides.get(module, self.default_level)
            expanded = expand_grant_level(level, actions)
            if expanded:
                resolved[module] = expanded
        return resolved


